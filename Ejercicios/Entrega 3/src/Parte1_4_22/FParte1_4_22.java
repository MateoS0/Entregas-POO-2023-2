    private void btnComenzarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnComenzarActionPerformed
        // TODO add your handling code here:
        String nombre;
        double sBasico, hTrabajo, sMensual;
        
        nombre = txtNombre.getText();
        sBasico = Double.parseDouble(txtValor.getText());
        hTrabajo = Double.parseDouble(txtHoras.getText());
        
        sMensual = sBasico*hTrabajo;
        
        if (sMensual>450000) {
            JOptionPane.showMessageDialog(this, "Nombre: "+nombre+"\nSalario mensual: "+sMensual);
        }
        else {
            JOptionPane.showMessageDialog(this, "Nombre: "+nombre);
        }
    }//GEN-LAST:event_btnComenzarActionPerformed

