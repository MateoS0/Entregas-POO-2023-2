    private void btnComenzarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnComenzarActionPerformed
        // TODO add your handling code here:
        double aA, bB, cC, disc, x1, x2;
        
        aA = Double.parseDouble(txtA.getText());
        bB = Double.parseDouble(txtB.getText());
        cC = Double.parseDouble(txtC.getText());
        
        disc = Math.pow(bB,2)-4*aA*cC;

        if (disc > 0) {
            x1 = (-bB+Math.sqrt(disc))/(2*aA);
            x2 = (-bB-Math.sqrt(disc))/(2*aA);
            JOptionPane.showMessageDialog(this, "X1: "+x1+"\nX2: "+x2);
        }
        else if (disc == 0) {
            x1 = -bB/(2*aA);
            JOptionPane.showMessageDialog(this, "X1: "+x1);
        }
        else {
            JOptionPane.showMessageDialog(this, "No hay soluciones reales.");
        }
    }//GEN-LAST:event_btnComenzarActionPerformed

